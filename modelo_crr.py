  d = 1/u
  p = ((1+r)-d)/(u-d)
  q = 1-p
  V1_sale = p*(vettore_finale[0])/(1+r)+q*(vettore_finale[1])/(1+r)
  V1_scende = p*(vettore_finale[1])/(1+r)+q*(vettore_finale[2])/(1+r)
  V0 = p*V1_sale/(1+r)+q*V1_scende/(1+r)
  print('Resultado Valoraci√≤n PUT CRR')
